package conversiones;

/**
 *
 * @author MEL esta es una clase entidad por que adamas tiene atributos y no operaciones
 */
public class Grados {
   private float centigrados;
   private float far;
   private float kelvin;

    public float getCentigrados() {
        return centigrados;
    }

    public void setCentigrados(float centigrados) {
        this.centigrados = centigrados;
    }

    public float getFar() {
        return far;
    }

    public void setFar(float far) {
        this.far = far;
    }

    public float getKelvin() {
        return kelvin;
    }

    public void setKelvin(float kelvin) {
        this.kelvin = kelvin;
    }
}
